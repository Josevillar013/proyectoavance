class Obstaculo
{
private:
    int Objeto;
    int Escenario;
    int Juego;
    int Payaso;

public:
    Obstaculo(std::string nombre)
    {
        this->Objeto = 0;
        this->Objeto = nombre;
        this->Escenario = 0;
        this->Escenario = nombre;
        this->Juego = 0;
        this->Juego = nombre;
        this->Payaso = 0;
        this->Payaso;
    }

    ~Obstaculo() {}
};